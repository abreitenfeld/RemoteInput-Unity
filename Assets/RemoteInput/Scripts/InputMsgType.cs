namespace RemoteInput
{
	public class InputMsgType
	{

		/// <summary>
		/// Internal message for communicating the acceleration.
		/// </summary>
		public const short Acceleration = 700;

		/// <summary>
		/// Internal message for communicating the compass measurement.
		/// </summary>
		public const short Compass = 701;

		/// <summary>
		/// Internal message for communicating the value of an axis.
		/// </summary>
		public const short Axis = 702;

		/// <summary>
		/// Internal message for communicating a mouse button has pressed.
		/// </summary>
		public const short MouseButtonPress = 703;
	
		/// <summary>
		/// Internal message for communicating a key has pressed.
		/// </summary>
		public const short KeyPress = 704;

		/// <summary>
		/// Internal message for communicating a button has pressed.
		/// </summary>
		public const short ButtonPress = 705;

		/// <summary>
		/// Internal message for communicating gyroscope measurement.
		/// </summary>
		public const short Gyroscope = 706;


	}
}

